package com.sh.project.vo;

public class CartVO {
	private int c_board;
	private int idx;
	private String name;
	private int num;
	private int price;
	private int total;
	
	public int getC_board() {
		return c_board;
	}
	public void setC_board(int c_board) {
		this.c_board = c_board;
	}
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	
	
}
